const mongoose = require("mongoose");
const Schema = mongoose.Schema;
const mongooseSlugPlugin = require("mongoose-slug-plugin");
const Category = require("../models/category");

const articleSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    imageUrl: {
      type: String,
      default: null,
      required: false,
    },
    imageAlt: {
      type: String,
      default: null,
      required: false,
    },
    content: {
      type: String,
      required: true,
    },
    category: {
      type: Category,
      required: true,
    },
    user: {
      id: {
        type: String,
        required: true,
      },
      name: {
        type: String,
        required: true,
      },
      imageUrl: {
        type: String,
        required: false,
      },
    },
    slug: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
    toJSON: {
      transform(doc, ret) {
        ret.id = ret._id;
        delete ret._id;
        delete ret.__v;
        delete ret.slug_history;
      },
    },
  }
);

mongoose.deleteModel("Article");
module.exports = mongoose.model("Article", articleSchema);
